    private void regNoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_regNoFocusLost

        //Check for existing registration number
        vmsDao.VehicleGet vrg = new vmsDao.VehicleGet();
        vrg.setvRegNo(regNo.getText());
        vrg.getVehicleDetails();
        if (vrg.isDataok() == true) {
            jButton5.setEnabled(false);

            regNo.setEditable(false);
            model.setEditable(false);
            color.setEditable(false);
            vType = "";

            JOptionPane.showMessageDialog(this, "Vehicle Details Found");
            
            jButton5.setEnabled(false);
            color.setFocusable(true);
        }
    }//GEN-LAST:event_regNoFocusLost

