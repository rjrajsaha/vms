    private void IssuePassActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_IssuePassActionPerformed

        vvName = vName.getText();
        vvDob = dob.getText();
        vvContact = contact.getText();
        vvAddress = address.getText();
        vvPerson = vPerson.getText();
        vvPurpose = purpose.getText();

        if (vvName.equals("") && vvDob.equals("") && vvContact.equals("") && vvAddress.equals("") && vvPerson.equals("") && vvPurpose.equals("")) {
            JOptionPane.showMessageDialog(this, "Fill Complete Form");
        } else {
            try {
                VisitorDao visitor = new VisitorDao(vvName, vvDob, vvContact, vvAddress, vvPerson, vvPurpose);
                visitor.createVisitor();
                vVisitorIDDao = visitor.getVisitorID();
                // Create a object of permanent pass class
                vmsDao.TPassDao tpass = new vmsDao.TPassDao(vRegNo, vVisitorIDDao);

                System.out.println("RegNo and Visitor" + vRegNo + "  " + vVisitorIDDao);

                tpass.createTempoPass();
                vTPassNo = tpass.getTPass();
                System.out.println("" + tpass.getTPass());

                

                //CheckIN immediately
                vmsDao.CheckInTime cint = new vmsDao.CheckInTime();
                cint.setvPassNo(vTPassNo);
                cint.checkIN();
                if (cint.isDataok() == true) {
                    JOptionPane.showMessageDialog(this, "Your Pass Number is " + vTPassNo);
                    

                }
                
            } catch (Exception x) {
                System.out.println("" + x);
            }
            
        }

    }//GEN-LAST:event_IssuePassActionPerformed

